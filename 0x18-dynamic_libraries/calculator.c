/**
 * add - Addition function.
 *
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: The sum of a and b.
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Substraction function.
 *
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: The difference of a and b.
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplication function.
 *
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: The product of a and b.
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Division function.
 *
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: The division of a and b.
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Module function.
 *
 * @a: First integer.
 * @b: Second integer.
 *
 * Return: The module of a and b.
 */
int mod(int a, int b)
{
	return (a % b);
}
